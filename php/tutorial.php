               <h1>How to build a PC Tutorial</h1>
               <h2 id="titleTutorial">Our comprehensive PC build guide will walk you through how to put together your own computer, to save some money.</h2>
               <div id="tutorialimage" align="center"><img src="../images/tutoimg1.jpg" class="tuto-img"></div>
             </div>

             <div id="describ">
             <p id="tutodetail" style="text-align: justify; font-size: 16px; padding-top: 15px;">Building a PC in 2018 remains to be a daunting endeavor for
               many consumers. But it doesn't have to be. Even if you've never picked up a screwdriver and tossed together a
               few components, this comprehensive guide will make you a PC building master in no time at all.<br><br>

             The best part about building a desktop PC is that it's a skill pretty much everyone will be able to learn through
             experience. It's also a fairly straightforward process too - the chances of messing up or really causing problems for
             yourself are slim. One of the most important parts of actually building a PC is picking the parts.<br><br>

           Before you even attempt to install components, it's paramount that you order internals that are compatible with
           one another and that provide enough computing power to accomplish desired tasks.
           For gaming, you'll want to go for an unlocked CPU and high-end GPU, while those who wish to simply watch videos and check social media will only need more affordable components.
         </p>
       </div>


       <div id="describ">
       <h2>Picking the right parts</h2>
       <div id="tutorialimage1" align="center"><img src="../images/tutoimg2.jpg" class="tuto-img"></div>

       <p id="tutodetail" style="text-align: justify; font-size: 16px; padding-top: 15px;">
         The most important part to get right is picking parts that do what you want and work with each other.
          The wrong parts or ones that happen to be incompatible with one another may cause issues, damage to other components
          or require time to be wasted with returning said products to retailers.We'll need a CPU, GPU, RAM, motherboard, and some storage to complete the barebones checklist.
       </p>
     </div>

     <div id="describ">
     <h2>Compatibility</h2>
     <div id="tutorialimage1" align="center"><img src="../images/tutoimg3.jpg" class="tuto-img"></div>
     <h3 style="font-weight:normal;">Here's what you're generally looking for with components:</h3>

     <p id="tutodetail" style="text-align: justify; font-size: 16px;">
       <ul>
         <li style="padding-top:10px;"><span style="font-weight:bold;">Case:</span> Cases come in different form factors, depending on the size of PC you wish to build. Less important overall.</li>
         <li style="padding-top:10px;"><span style="font-weight:bold;">PSU:</span> Needs to supply enough stable power (500W is usually a good value).</li>
         <li style="padding-top:10px;"><span style="font-weight:bold;">CPU:</span> You have the choice of AMD and Intel for desktop processors.
           Take a look at our best guide and take note of the socket they use as you'll need to match the socket for the motherboard.</li>
         <li style="padding-top:10px;"><span style="font-weight:bold;">Motherboard:</span> A motherboard simply needs to match the same socket type of the CPU. Chipset and other features are all down to pricing and preference.</li>
         <li style="padding-top:10px;"><span style="font-weight:bold;">RAM:</span> The faster the frequency, generally the better the RAM, though you'll need to make sure your motherboard can support the same clock speed or it'll be limited. Most new boards support DDR4.</li>
          <li style="padding-top:10px;"><span style="font-weight:bold;">Storage:</span> Entirely down to personal requirements, though we always recommend an SSD for installing Windows.</li>
          <li style="padding-top:10px;"><span style="font-weight:bold;">GPU:</span> This is optional and is only really needed if you plan to do some gaming or intensive workloads. In that case, spend as much as you can.</li>
       </ul>
     </p>
   </div>

   <div id="describ">
     <h2>Trusted tools</h2>
     <div id="tutorialimage1" align="center"><img src="../images/tutoimg4.jpg" class="tuto-img"></div>
     <h3 style="font-weight:normal;">The bare essentials you'll need to build a PC including the following:</h3>

     <p id="tutodetail" style="text-align: justify; font-size: 16px;">
       <ul>
         <li style="padding-top:10px;">Philips (preferably magnetic) screwdriver.</li>
         <li style="padding-top:10px;">LED light source.</li>
         <li style="padding-top:10px;">Flat workspace (no carpet).</li>
         <li style="padding-top:10px;">Anti-static mat.</li>
         <li style="padding-top:10px;">Anti-static wristband.</li>
       </ul>
     </p>
   </div>

   <div id="describ">
     <h2>Build it better</h2>

     <p id="tutodetail" style="text-align: justify; font-size: 16px;">
       We've arranged this guide in an order that makes sense in most cases, though it may not be optimal for every PC case.
      You'll need to check the layout and see which components will need to be installed first, but usually, the power supply is the best way to go.
     </p>
     <p id="tutodetail" style="text-align: justify; font-size: 16px;"><ol>
       <li style="padding-top:10px;">Installing the PSU</li>
       <li style="padding-top:10px;">Installing the CPU</li>
       <li style="padding-top:10px;">Installing the RAM</li>
       <li style="padding-top:10px;">Installing a motherboard</li>
       <li style="padding-top:10px;">Installing storage drives</li>
       <li style="padding-top:10px;">Plugging everything in</li>
       <li style="padding-top:10px;">Adding case fans</li>
       <li style="padding-top:10px;">Installing a GPU</li>
     </ol></p>
   </div>


   <div id="describ">
     <h2>1. Installing the PSU</h2>

     <p id="tutodetail" style="text-align: justify; font-size: 16px;">
       The PSU is the most important component of any PC. It provides the juice required for all the computing magic to take place.
        Depending on the case you've purchased, we'll need to install the unit with the fan facing up or down.
        To determine which is most optimal, check if you have a vent at the bottom of the case. If so, face the PSU fan downwards.
     </p>
     <p id="tutodetail" style="text-align: justify; font-size: 16px;"><ol>
       <li style="padding-top:10px;">Slide the <span style="font-weight:bold;">PSU</span> into the mounting area with the fan facing up or down.</li>
       <li style="padding-top:10px;">Screw in bundled screws (should come with the case) in the <span style="font-weight:bold;">four holes</span> at the rear of the case.</li>
       <li style="padding-top:10px;">Ensure the unit is <span style="font-weight:bold;">switched off</span> using the rear switch.</li>
       <li style="padding-top:10px;">Plug the PSU into a free socket to <span style="font-weight:bold;">ground the PC.</span></li>
       <li style="padding-top:10px;">Bind all the <span style="font-weight:bold;">power cables</span> coming out of the PSU together and put them to one side</li>
     </ol></p>
     <div id="tutorialimage1" align="center"><img src="../images/psu.jpg" class="tuto-img"></div>
     <h3 style="font-weight:normal;">Depending on the PSU you've purchased, it may be one of the following:</h3>

     <p id="tutodetail" style="text-align: justify; font-size: 16px;">
       <ul>
         <li style="padding-top:10px;"><span style="font-weight:bold;">Fully modular:</span> No cables are permanently connected to the PSU.</li>
         <li style="padding-top:10px;"><span style="font-weight:bold;">Semi-modular:</span> Some cables are permanently fixed, usually ATX and CPU power.</li>
         <li style="padding-top:10px;"><span style="font-weight:bold;">Non-modular:</span> Every cable is connected and cannot be removed from the PSU.</li>
       </ul>
     </p>
   </div><hr>

   <div id="describ">
     <h2>2. Installing the CPU</h2>

     <p id="tutodetail" style="text-align: justify; font-size: 16px;">
       We can now insert the CPU into the motherboard. This is a super-easy process, regardless as to whether you're rocking an AMD or Intel chip.
     </p>
     <p id="tutodetail" style="text-align: justify; font-size: 16px;"><ol>
       <li style="padding-top:10px;">Unpack the <span style="font-weight:bold;">motherboard</span> from its packaging.</li>
       <li style="padding-top:10px;">Place the motherboard atop its <span style="font-weight:bold;">cardboard box.</span> (It's safe here.)</li>
       <li style="padding-top:10px;">Open the <span style="font-weight:bold;">CPU latch.</span></li>
       <li style="padding-top:10px;">Remove the <span style="font-weight:bold;">plastic cap.</span></li>
       <li style="padding-top:10px;">Insert the <span style="font-weight:bold;">CPU</span>, matching notches and indicators to the socket. (Do not apply pressure, it should lie flush without force.)</li>
       <li style="padding-top:10px;">Close the <span style="font-weight:bold;">latch</span> to secure the CPU. (This step will require some amount of pressure.)</li>
     </ol></p>
     <div id="tutorialimage1" align="center"><img src="../images/cpu.jpg" class="tuto-img"></div>

     <p id="tutodetail" style="text-align: justify; font-size: 16px;">
       Now we can use the thermal paste and attach the CPU cooler to complete everything processor-related. Depending on the cooler you've purchased,
        you may have a layer of thermal paste already applied. If not, we'll need to do this before installing the cooler. (Tip: less is more.) <br><br>

        Especially with aftermarket coolers both water and air, you'll need to pay close attention to the manual that comes with the product.
         This is why it's better to do all these steps with the motherboard outside the PC case for easier handling.
     </p>
   </div><hr>

   <div id="describ">
     <h2>3. Installing the RAM</h2>

     <p id="tutodetail" style="text-align: justify; font-size: 16px;">
      RAM is a sensitive component and as such should be handled with care. It's recommended that contact is avoided with the contact pins on
      the underside of each module. The RAM slots on a motherboard are located to the right of the CPU but do check the motherboard manual as to
      which RAM slots are to be used, depending on how many sticks you have. A motherboard with four slots or more, you'll want to populate slots one and two first.
     </p>
     <div id="tutorialimage1" align="center"><img src="../images/ram.jpg" class="tuto-img"></div>
     <p id="tutodetail" style="text-align: justify; font-size: 16px;"><ol>
       <li style="padding-top:10px;">Unclip the <span style="font-weight:bold;">RAM</span> locks on the motherboard module slots.</li>
       <li style="padding-top:10px;">Match the RAM <span style="font-weight:bold;">module notches</span> to those found in the slots on the motherboard</li>
       <li style="padding-top:10px;"><span style="font-weight:bold;">Insert the module</span> carefully, using light even force across the stick.</li>
       <li style="padding-top:10px;">After it inserts into the slot, push down on either side of the RAM module until it <span style="font-weight:bold;">securely clicks</span> into place.</li>
     </ol></p>

   </div><hr>

   <div id="describ">
     <h2>4. Installing the Motherboard</h2>

     <p id="tutodetail" style="text-align: justify; font-size: 16px;">
      With the CPU, the cooler, and RAM modules installed, the motherboard is ready to be screwed to the back plate inside the case.
       First, we need to install the I/O shield, a long piece of metal that has cutouts for the all the rear ports and connections.
       This is optional but recommended accessory helps provide electromagnetic interference (EMI) protection.
     </p>
     <div id="tutorialimage1" align="center"><img src="../images/motherboard.jpg" class="tuto-img"></div>
     <p id="tutodetail" style="text-align: justify; font-size: 16px;"><ol>
       <li style="padding-top:10px;">Place the I/O shield to the rear cutout in the PC case and click into place. (This can be super-fiddly.)</li>
       <li style="padding-top:10px;">Hover the motherboard atop the back plate to see where standoffs need to be screwed in. Different sizes need different holes.<br><br>
       Standoffs are the screws that are installed on the motherboard backplate, which then allows the motherboard to rest atop them and for screws to secure the board.</li>
       <li style="padding-top:10px;">Install the standoffs for the motherboard, if not already pre-installed. (Some backplates may feature labels next to holes to guide you.)</li>
       <li style="padding-top:10px;">Lower the motherboard onto the standoffs.</li>
       <li style="padding-top:10px;">Tighten the screws, but do not overtighten them â€” just enough to secure the board in place.</li>
       <li style="padding-top:10px;">Check the sides of the motherboard by lightly pulling to make sure every inch has been secured.</li>
     </ol></p>

   </div><hr>

   <div id="describ">
     <h2>5. Installing storage drives</h2>

     <p id="tutodetail" style="text-align: justify; font-size: 16px;">
      Storage drives are important devices that hold not only the operating system but also all programs, personal media, and all other data.
      It's recommended to use an SSD (2.5-inch) drive for the main OS partition, which enables for quick booting and solid performance. Data can be stored on traditional (3.5-inch) mechanical drives.<br><br>

      Depending on the case and how drive bays are configured, 2.5- and 3.5-inch drives may be able to be installed at the same points or using dedicated brackets.
       Be sure to double check the PC case manual. We'll go over how your PC case may have both configured below.
     </p>
     <div id="tutorialimage1" align="center"><img src="../images/ssd.jpg" class="tuto-img"></div>
     <h3 style="font-weight:normal;">2.5 Inch SSD</h3>
     <p id="tutodetail" style="text-align: justify; font-size: 16px;"><ol>
       <li style="padding-top:10px;">Take the SSD bracket and attach the drive to it, lining up the screw holes.</li>
       <li style="padding-top:10px;">Screw in the drive using appropriate screws.</li>
       <li style="padding-top:10px;">Attach the bracket and secure it to the case.</li>
       </ol></p>


       <h3 style="font-weight:normal;">3.5 Inch HDD</h3>
       <p id="tutodetail" style="text-align: justify; font-size: 16px;"><ol>
       <li style="padding-top:10px;">Extract the HDD bracket from one of the bays.</li>
       <li style="padding-top:10px;">Slide the mechanical drive into the bracket.</li>
       <li style="padding-top:10px;">Secure the drive to said bracket using screws or screwless mechanism.</li>
       <li style="padding-top:10px;">Slide the bracket and drive back into the bay.</li>
     </ol></p>

   </div><hr>

   <div id="describ">
     <h2>6. Plugging everything in</h2>

     <p id="tutodetail" style="text-align: justify; font-size: 16px;">
      With all major components now installed,
       it's time to hook everything up to the power supply and motherboard. Generally speaking, it's best to start with the power to the motherboard itself.
     </p>
     <div id="tutorialimage1" align="center"><img src="../images/cable.jpg" class="tuto-img"></div>
     <p id="tutodetail" style="text-align: justify; font-size: 16px;"><ol>
       <li style="padding-top:10px;"><span style="font-weight:bold;">Motherboard:</span> The PSU has two cables for the board: 24-pin ATX and 8-pin CPU. Route these behind the back plate, using available grommets and cutouts.</li>
       <li style="padding-top:10px;"><span style="font-weight:bold;">Storage drives:</span> Most SSDs and HDDs today use SATA connectors. These are 15-pin flat connectors with a dip on one side to prevent plugging them in the wrong way.
          Route these from the PSU to the drive bays and mounts, using more than one lead from the PSU if required.</li>
       </ol></p>

       <p id="tutodetail" style="text-align: justify; font-size: 16px;">
       Next, we need to connect the storage drives to the motherboard, using SATA data cables.
        These are essentially smaller versions of their power siblings that carry data instead. The motherboard usually has SATA ports located to
        the lower-left hand-side. It doesn't matter which ports are used. Much like the power cables, route these through cut-outs and grommets to the drive bays and mounts.</p>

        <p id="tutodetail" style="text-align: justify; font-size: 16px;">
        We now have power to our storage drives and to the motherboard, as well as data channels between drives and the main board.
         All that's really left is to connect front panel I/O and other optional connectors (front panel USB, HD audio, etc.).
         Connecting the front panel I/O, which includes the power switch, reset switch, HDD activity LED, and power LED can be a tricky step.</p>

         <p id="tutodetail" style="text-align: justify; font-size: 16px;">
        These pins stand up from the motherboard, which requires a careful approach when plugging in the small connectors.
         The motherboard manual should provide information as to the layout of these pins and what they correspond.</p>


   </div><hr>

   <div id="describ">
     <h2>7. Adding case Fans</h2>

     <p id="tutodetail" style="text-align: justify; font-size: 16px;">
      For added cooling capacity (and unlocking room for overclocking or more stable operation), throwing in an extra fan or two is worthwhile.
       PC cases may come with fans pre-installed, but if not it's simple to screw them into fan mounts.
        Just be sure to align them correctly and have the units pointing the correct way. Fans can have small arrows on the side that show which way the blades spin and where airflow will be directed.
     </p>
     <div id="tutorialimage1" align="center"><img src="../images/fan.jpg" class="tuto-img"></div>
       <p id="tutodetail" style="text-align: justify; font-size: 16px;">
       Note: some cases may require you to install fans before inserting the motherboard.
       Fans can either be plugged into a motherboard or to the PSU directly using an adapter.
       We'd always recommend the motherboard due to the ability for it to control the fans intelligently depending on system temperatures.
        Fan headers can be located on the motherboard itself where the 3 (or 4-) pin connectors can be inserted.</p>
   </div><hr>


   <div id="describ">
     <h2>8. Installing a GPU</h2>

     <p id="tutodetail" style="text-align: justify; font-size: 16px;">
      Need some extra power for gaming and intense workloads? A GPU is a must-have. As an added bonus, it's really easy to install a graphics card.
     </p>
     <div id="tutorialimage1" align="center"><img src="../images/gpu.jpg" class="tuto-img"></div>

         <p id="tutodetail" style="text-align: justify; font-size: 16px;"><ol>
         <li style="padding-top:10px;">Unscrew the rear PCI brackets that align with the PCI slot you will use.</li>
         <li style="padding-top:10px;">Check that the motherboard GPU card latch is open.</li>
         <li style="padding-top:10px;">Line up the GPU to the PCIe slot on the motherboard.</li>
         <li style="padding-top:10px;">Carefully insert the card and push down once it has made contact with the slot, listening out for a click of the latch securing.</li>
         <li style="padding-top:10px;">Use the rear bracket screws to secure the GPU to the PC case for added stability.</li>
       </ol></p>
   </div><hr>

   <div id="describ">
     <h2>Power on</h2>

     <p id="tutodetail" style="text-align: justify; font-size: 16px;">
    All that hard work will now pay off. It's time to boot up the PC and make sure everything is working fine now that all components are inside the case.
    Switch on the PSU and hit the power switch. You should now be greeted by the BIOS POST screen, asking for an OS to be installed if one is not detected.
     You'll now need to follow OS installation instructions. If you run into problems, it's time to do a little troubleshooting.
     </p>
